







def moving_avg(input, window_size):
    result = []
    moving_sum = sum(input[:window_size])
    result.append(moving_sum / window_size)
    for i in range(len(input) - window_size):
        moving_sum += (input[i + window_size] - input[i])
        result.append(moving_sum / window_size)
    return result

